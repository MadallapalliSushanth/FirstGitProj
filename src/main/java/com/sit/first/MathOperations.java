public class MathOperations {
	
	public int addition(int a, int b) {
		int result= a+b;
		return result;
	}
	
	public double sub(double a,double b) {
		double c=a-b;
		return c;
	}
	public double multiplication(int a) {
		double multiplication=a*a;
		return a*a;
	}
	public double multiplication1(int a) {
		double multiplication=a*a*a;
		return a*a*a;
	}
}
